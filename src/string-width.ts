const ESC = "\x1b";
const CSI = `${ESC}[`;

const ansiRegex = new RegExp(
  `(${ESC}(?:\\[[0-9;]*[a-zA-Z]|\\][^\\x07]*\\x07|\\[[0-9;]*m))`,
  "g",
);

function stripAnsi(str: string): string {
  return str.replace(ansiRegex, "");
}

